def quadratic_biggest_fish(fishes)
  #run two loops
  longest_fish = ""
  fishes.each_with_index do |fish1, index1|
    fishes.each_with_index do |fish2, index2|
      if fish2.length > fish1.length
        longest_fish = fish2
      end
    end
  end
  longest_fish
end

